# OPERADORES NUMÉRICOS EN PYTHON
# ------------------------------------

print("---Operadores básicos---")

# suma
a=20
b=10

suma = a+b
print(f"Suma {a} + {b}= {suma}")

#Resta (-)
resta = a - b
print(f"Resta {a} - {b}= {resta}")

#Multiplicación
multiplicacion= a * b
print(f"Multiplicación {a} * {b}= {multiplicacion}")

#Division
division = a/b
print(f"División {a} / {b} = {division}")

#División entera
#Devuelve solo la parte entera de la división
division_entera= a // b
print(f"División entera {a} // {b} = {division_entera}")

print("\n===== Operaciones Especiales =====")
#Módulo (%)
#Devuelve el resto de la división
modulo = a % b
print(f"Módulo {a} % {b} = {modulo}")

#Exponente
#Eleva un número a una potencia
exponente = a ** b
print(f"Exponenete {a} ** {b} = {exponente}")




